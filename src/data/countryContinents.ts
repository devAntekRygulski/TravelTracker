const COUNTRY_NAME_CONTINENTS: Record<string, Continent> = {
  'N. Cyprus': 'Asia',
  Somaliland: 'Africa',
  Kosovo: 'Europe',
};

export function getCountryContinent(
  countryId: string,
  countryName?: string,
): Continent | undefined {
  return COUNTRY_CONTINENTS[countryId] ?? (countryName ? COUNTRY_NAME_CONTINENTS[countryName] : undefined);
}

export function countVisitedContinents(
  visitedCountryIds: Iterable<string>,
  countryNamesById?: Record<string, string>,
): number {
  const continents = new Set<Continent>();

  for (const countryId of visitedCountryIds) {
    const continent = getCountryContinent(countryId, countryNamesById?.[countryId]);
    if (continent) {
      continents.add(continent);
    }
  }

  return continents.size;
}
